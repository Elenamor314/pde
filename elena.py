        y = y.to(self.device)

        x = (x - self.mean) / self.std
        y = (y - self.mean) / self.std

        self.activation = {}
        self.base_model(x)
        x_features = self.activation[self.selected_layer]

        self.activation = {}
        self.base_model(y)
        y_features = self.activation[self.selected_layer]

